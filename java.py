module = Module()

#Uses hide for private to improve accuracy
@module.capture(rule = '(pro|pub|last|trans|ab|hide)')
def java_programming_access_modifier(m) -> str:
    word = m[0]
    if word == 'pub':
        return 'public '
    elif word == 'hide':
        return 'private '
    elif word == 'pro':
        return 'protected '
    elif word == 'last':
        return 'final '
    elif word == 'trans':
        return 'transient '
    elif word == 'ab':
        return 'abstract'

@module.capture(rule = '<user.java_programming_access_modifier>|prose|pubs|hides')
def java_programming_function_access_modifier(m) -> str:
    word = m[0]
    if word == 'prose':
        return 'protected static '
    elif word == 'hides':
        return 'private static '
    elif word == 'pubs':
        return 'public static '
    else:
        return m.java_programming_access_modifier

@module.capture(rule = "{user.code_type}|<user.text>")
def java_programming_type(m) -> str:
    """Java built in or user type"""
    with suppress(Exception):
        return m.code_type
    return actions.user.fire_chicken_convert_text_to_pascal_case(m.text)


@module.action_class
class Actions:
    def java_programming_build_new():
        '''Given a data type and variable name, produces new assignment'''
        current_line = actions.user.generic_programming_get_comma_separated_line_ignoring_standard_separators()
        if len(current_line) != 2:
            return 
        data_type = current_line[0]
        variable_name = current_line[1]
        constructor_datatype = get_data_type_without_generic_specifics(data_type)
        actions.insert(f'{data_type} {variable_name}{apply_spacing_setting_to(assignment_style, "=")}'
            f'new {constructor_datatype}{get_object_parentheses().get_text()};')
        for iteration in range(2):
            actions.edit.left() 
    def java_programming_system_out_method(name: str):
        '''Inserts the specified system out method'''
        container = get_function_call_parentheses()
        actions.insert(f'System.out.{name}{container.get_text()};')
        actions.edit.left()
        container.enter_from_right()
    def java_programming_indicate_superclass(name: str):
        '''Inserts text indicating the superclass'''
        actions.insert(' extends ')
        actions.user.fire_chicken_insert_formatted_text(name, 'hammer')
    def java_programming_indicate_implements(interface_name: str):
        '''Inserts text indicating that the class implements the specified interface'''
        actions.insert(' implements ')
        actions.user.fire_chicken_insert_formatted_text(interface_name, 'hammer')
    def java_programming_insert_assignment(variable_name: str):
        """Inserts a java variable assignment"""
        actions.user.fire_chicken_insert_formatted_text(variable_name, 'camel')
        actions.user.generic_programming_insert_assignment()
        actions.user.fire_chicken_insert_after(";")
        

def get_data_type_without_generic_specifics(name):
    if '<' not in name:
        return name
    else:
        name_without_generics = name.split('<')[0]
        result = name_without_generics + '<>'
        return result


